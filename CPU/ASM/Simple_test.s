START:
  LI R2 0X0001
NEXT_ROW:
  LI R0 0X000F
  NEXT_COL:
    LI R1 0X0050

    SLL R4 R0 0X0000 ;R4 IS POS HIGH 8 DIGIT
    LI R5 0X00FF
    AND R5 R1 ;R5 IS POS LOW 8 DIGIT
    OR R4 R5  ;R4 IS POS FULL
    ;R6 IS 0XBF0A
    LI R6 0X00BF
    SLL R6 R6 0X0000
    ADDIU R6 0X000A
    ;SAVE TO 0XBF0A
    SW R6 R4 0X0000

    ;GPUDATA
    LI R6 0X00BF
    SLL R6 R6 0X0000
    ADDIU R6 0X000D
    ;SAVE TO 0XBF0D
    SW R6 R2 0X0000

    ADDIU R1 0XFFFF
    NOP
    BNEZ R1 NEXT_COL
    NOP
  ADDIU R0 0XFFFF
  NOP
  BNEZ R0 NEXT_ROW
  NOP
  LI R2 0X0000
NEXT_ROW:
  LI R7 0X000F
  LI R0 0X001E
  NEXT_COL:
    LI R1 0X0050

    SLL R4 R0 0X0000 ;R4 IS POS HIGH 8 DIGIT
    LI R5 0X00FF
    AND R5 R1 ;R5 IS POS LOW 8 DIGIT
    OR R4 R5  ;R4 IS POS FULL
    ;R6 IS 0XBF0A
    LI R6 0X00BF
    SLL R6 R6 0X0000
    ADDIU R6 0X000A
    ;SAVE TO 0XBF0A
    SW R6 R4 0X0000

    ;GPUDATA
    LI R6 0X00BF
    SLL R6 R6 0X0000
    ADDIU R6 0X000D
    ;SAVE TO 0XBF0D
    SW R6 R2 0X0000

    ADDIU R1 0XFFFF
    NOP
    BNEZ R1 NEXT_COL
    NOP
  ADDIU R0 0XFFFF
  ADDIU R7 0XFFFF
  NOP
  BNEZ R7 NEXT_ROW
  NOP
  B START
  NOP

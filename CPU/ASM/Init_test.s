START:
  LI R3 0X0000
NEXT_ROW:
  LI R0 0X001E
  ADDIU R0 0XFFFF
  ADDIU R3 0X0001
  NEXT_COL:
    LI R1 0X0050
    ADDIU R1 0XFFFF

    ;ROW R0, COL R1
    LI R2 0X0001  ;COLOR
    ADDIU R3 0XFFFF
    AND R2 R3

    ;GPUPOS
    SLL R4 R0 0X0000 ;R4 IS POS HIGH 8 DIGIT
    OR R4 R1  ;R4 IS POS FULL

    ;SAVE TO 0XBF0A
    LI R6 0X00BF
    SLL R6 R6 0X0000
    ADDIU R6 0X000A
    SW R6 R4 0X0000

    ;SAVE TO 0XBF0D
    LI R6 0X00BF
    SLL R6 R6 0X0000
    ADDIU R6 0X000D
    SW R6 R2 0X0000

    NOP
    BNEZ R1 NEXT_COL
    NOP
  NOP
  BNEZ R0 NEXT_ROW
  NOP

